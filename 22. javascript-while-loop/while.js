// While loop (initialisation, condition, operation)

let j = 0;
while (j<=10) {
    console.log(j);
    j++;
}

let arrayOne = [1,2,3,4,5];
let arr = 0;
while(arr<=arrayOne.length) {
    console.log(`The value is ${arr}`);
    arr++;
}

// Do While loop (initialisation, operation, condition)
let marks = 25;
do {
    console.log(marks);
    marks++;
} while (marks<=30);

